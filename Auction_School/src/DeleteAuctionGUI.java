    public void getAuction() {
        Lot lot = auction.getTheLot(Integer.parseInt(auctionNumber_JTextField.getText()));
        if (lot != null) {
            auctionDescription_JTextArea.setForeground(Color.BLACK);
            auctionDescription_JTextArea.setText(lot.getDescription());
            selectedAuction = lot;
            deleteAuction_JButton.setEnabled(true);
        } else {
            selectedAuction = null;
            auctionDescription_JTextArea.setForeground(Color.RED);
            auctionDescription_JTextArea.setText("Auction number " + auctionNumber_JTextField.getText() + " does not exist.");
            deleteAuction_JButton.setEnabled(false);
        }
    }

